public class WeixinGuessRiddleListEntity {
    private String id;
    private String title;
    private String startTime;
    private String endTime;
    private double allFlow;
    private double riddleFlow;
    private double getFlow;
    private int num;
    private String state;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getStartTime() {
        return startTime;
    }

    public void setStartTime(String startTime) {
        this.startTime = startTime;
    }

    public String getEndTime() {
        return endTime;
    }

    public void setEndTime(String endTime) {
        this.endTime = endTime;
    }

    public double getAllFlow() {
        return allFlow;
    }

    public void setAllFlow(double allFlow) {
        this.allFlow = allFlow;
    }

    public double getRiddleFlow() {
        return riddleFlow;
    }

    public void setRiddleFlow(double riddleFlow) {
        this.riddleFlow = riddleFlow;
    }

    public double getGetFlow() {
        return getFlow;
    }

    public void setGetFlow(double getFlow) {
        this.getFlow = getFlow;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }
}
